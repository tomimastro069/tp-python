"""11- Pedir dos palabras por teclado, indicar si son iguales."""

palabra_1= input("Ingrese la primer palabra: ")
palabra_2= input("Ingrese la segunda palabra: ")


if palabra_1 == palabra_2:
    print(f"la palabra 1: {palabra_1} es igual a la palabra 2: {palabra_2}")
else:
    print(f"la palabra 1: {palabra_1} es diferente a la palabra 2: {palabra_2}")